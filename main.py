from timeit import timeit
from typing import List

from functools import wraps
import time


def timeit(func):
    @wraps(func)
    def timeit_wrapper(*args, **kwargs):
        start_time = time.perf_counter()
        result = func(*args, **kwargs)
        end_time = time.perf_counter()
        total_time = end_time - start_time
        print(f'Function {func.__name__} Took {total_time:.4f} seconds')
        return result

    return timeit_wrapper


class Solution:
    @timeit
    def threeSum(self, nums: List[int]) -> List[List[int]]:
        nums.sort()
        #print(nums)
        solution = {}
        n = len(nums)

        for i in range(n - 2):
            if i > 0 and nums[i] == nums[i - 1] and nums[i] == nums[i + 1] and nums[i + 1] == nums[i + 2]:
                continue
            j = i + 1
            k = n - 1
            while j < k:
                total = nums[i] + nums[j] + nums[k]
                #print(f'i={i}:{nums[i]},j={j}:{nums[j]},k={k}:{nums[k]},Total={total}')

                if total == 0:
                    key = f"{nums[i]}-{nums[j]}-{nums[k]}"
                    solution[key] = [nums[i], nums[j], nums[k]]
                    j += 1
                elif total < 0:
                    j += 1
                else:
                    k -= 1
        r = [v for v in solution.values()]
        return r


